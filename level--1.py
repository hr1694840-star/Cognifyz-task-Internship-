# Task--(String Reversal)
# create a python function that takes a string as input and return the reverse of string. 
# For example, if the input is "hello" the function should return "olleh".

def reverse_string(s):
   # Example: "hello" => "olleh"
    return s[::-1]
print("String Reversal")
print(reverse_string("hello"))  # olleh
print("------")
